# variable slapping

x = 10 # x is equal to 10.

y = 20 # y is equal to 20.

print("x = " + str(x)) # We printed x.

print("y = " + str(y)) # We printed y.

temp = x # We created temp variable and assigned x to temp variable.

x = y # We assigned y to x.

y = temp # We assigned temp variable to y.

print("x = " + str(x)) # We printed x.

print("y = " + str(y)) # We printed y.