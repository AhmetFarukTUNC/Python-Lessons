set1 = {1,"Understandable Economy",2.5} # We defined a set assigned to set1 this set.

set1.add("Hello") # We added "Hello" to end of set but order can change.

print(set1) # We printed value of set1.

set1.discard("Hello") # We removed "Hello" from set1.

print(set1)

set1.add("I am Ahmet.") # We added "I am Ahmet" to end of set.

print(set1) # We printed value of set1.

set1.remove("I am Ahmet.") # We removed "Ahmet" from set1.

print(set1) # We printed value of set1.